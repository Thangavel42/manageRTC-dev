const stats = {
  created: 0,
  updated: 0,
  skipped: 0,
  errors: [],
};

async function seedPages() {
  try {
    console.log('üîå Connecting to MongoDB...');
    console.log(`   Database: ${dbName}`);
    await mongoose.connect(uri, { dbName });
    console.log('‚úÖ Connected!\n');

    console.log('üì¶ Starting Pages Seed...\n');
    console.log('='.repeat(70));

    for (const pageDef of pageDefinitions) {
      try {
        // Check if page already exists
        let page = await Page.findOne({ name: pageDef.name });

        if (page) {
          // Update existing page (preserve some fields if needed)
          Object.assign(page, {
            displayName: pageDef.displayName,
            description: pageDef.description,
            route: pageDef.route,
            icon: pageDef.icon,
            moduleCategory: pageDef.moduleCategory,
            sortOrder: pageDef.sortOrder,
            isSystem: pageDef.isSystem,
            updatedAt: new Date(),
          });
          await page.save();
          stats.updated++;
          console.log(`‚úèÔ∏è  Updated: ${pageDef.displayName} (${pageDef.name})`);
        } else {
          // Create new page
          page = new Page(pageDef);
          await page.save();
          stats.created++;
          console.log(`‚úÖ Created: ${pageDef.displayName} (${pageDef.name})`);
        }
      } catch (error) {
        console.error(`‚ùå Error processing ${pageDef.name}: ${error.message}`);
        stats.errors.push({ page: pageDef.name, error: error.message });
      }
    }

    console.log('\n' + '='.repeat(70));
    console.log('üìä SEED SUMMARY');
    console.log('='.repeat(70));
    console.log(`‚úÖ Created: ${stats.created}`);
    console.log(`‚úèÔ∏è  Updated: ${stats.updated}`);
    console.log(`‚è≠Ô∏è  Skipped: ${stats.skipped}`);
    console.log(`‚ùå Errors: ${stats.errors.length}`);

    if (stats.errors.length > 0) {
      console.log('\n‚ùå Error Details:');
      stats.errors.forEach(err => {
        console.log(`   - ${err.page}: ${err.error}`);
      });
    }

    // Verify pages
    console.log('\nüìã Pages in Database (grouped by category):');
    const groupedPages = await Page.getGroupedByModule();
    groupedPages.forEach(group => {
      const category = group._id || 'Other';
      console.log(`\n   ${category.toUpperCase()} (${group.count} pages):`);
      group.pages.slice(0, 5).forEach(page => {
        console.log(`      ‚úÖ ${page.displayName} (${page.name})`);
      });
      if (group.pages.length > 5) {
        console.log(`      ... and ${group.pages.length - 5} more`);
      }
    });

    console.log('\n‚úÖ Pages seed complete!');

    await mongoose.disconnect();

  } catch (error) {
    console.error('\n‚ùå Seed Error:', error.message);
    console.error(error.stack);
    await mongoose.disconnect();
    process.exit(1);
  }
}

seedPages();
